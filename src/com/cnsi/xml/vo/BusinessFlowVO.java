package com.cnsi.xml.vo;

public class BusinessFlowVO {

	private String useCaseName;
	private String stepName;
	private String flow;
	private String actorDoes;
	private String fieldRules;
	private String systemDoes;
	
	
	
	public String getUseCaseName() {
		return useCaseName;
	}
	public void setUseCaseName(String useCaseName) {
		this.useCaseName = useCaseName;
	}
	public String getStepName() {
		return stepName;
	}
	public void setStepName(String stepName) {
		this.stepName = stepName;
	}
	public String getFlow() {
		return flow;
	}
	public void setFlow(String flow) {
		this.flow = flow;
	}
	public String getActorDoes() {
		return actorDoes;
	}
	public void setActorDoes(String actorDoes) {
		this.actorDoes = actorDoes;
	}
	public String getFieldRules() {
		return fieldRules;
	}
	public void setFieldRules(String fieldRules) {
		this.fieldRules = fieldRules;
	}
	public String getSystemDoes() {
		return systemDoes;
	}
	public void setSystemDoes(String systemDoes) {
		this.systemDoes = systemDoes;
	}
	
	
	
	
}
