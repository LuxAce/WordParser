package com.cnsi.xml.vo;

public class OtherUseCasesVO {

	
	private String useCaseID;
	private String useCaseName;
	private String useCaseDSD;
	private String useCasePurpose;
	
	
	
	public String getUseCaseID() {
		return useCaseID;
	}
	public void setUseCaseID(String useCaseID) {
		this.useCaseID = useCaseID;
	}
	public String getUseCaseName() {
		return useCaseName;
	}
	public void setUseCaseName(String useCaseName) {
		this.useCaseName = useCaseName;
	}
	public String getUseCaseDSD() {
		return useCaseDSD;
	}
	public void setUseCaseDSD(String useCaseDSD) {
		this.useCaseDSD = useCaseDSD;
	}
	public String getUseCasePurpose() {
		return useCasePurpose;
	}
	public void setUseCasePurpose(String useCasePurpose) {
		this.useCasePurpose = useCasePurpose;
	}
}
